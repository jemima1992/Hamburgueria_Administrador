    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
      String Login = ""+jTable1.getValueAt(jTable1.getSelectedRow(), 1);
      conex.conexao();
      conex.executaSql(" Select * from acesso where Login like'"+Login+"'");
      try {
          conex.rs.first();
          jComboBox2.setSelectedItem(conex.rs.getString("ID"));
          jTextField2.setText(conex.rs.getString("Login"));
          jPasswordField1.setText(conex.rs.getString("senha"));
  
      } catch (SQLException ex) {
          JOptionPane.showMessageDialog(null,"Erro ao selecionar dados"+ex);
      }
      
      conex.desconecta();
      jButton1.setEnabled(false);
      jButton3.setEnabled(true);
      jButton4.setEnabled(true);
      jButton5.setEnabled(true);
      jButton2.setEnabled(false);
      jComboBox2.setEnabled(false);
      jTextField2.setEnabled(false);
      jPasswordField2.setEnabled(false);
      jPasswordField1.setEnabled(false);
      
    }//GEN-LAST:event_jTable1MouseClicked


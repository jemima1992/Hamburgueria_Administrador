    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Preencha o usuário para continuar");
            jTextField2.requestFocus();
        }else if(jPasswordField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,"Preencha o Confirmar Senha para continuar");
            jPasswordField2.requestFocus();
        }else if(jPasswordField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,"Preencha a SENHA para continuar");
            jPasswordField1.requestFocus();
        }else 
        if(jPasswordField1.getText().equals(jPasswordField2.getText())){
        if (flag == 1) {
            mod.setID((String) jComboBox2.getSelectedItem());
            mod.setLogin(jTextField2.getText());
            mod.setSenha(jPasswordField1.getText());
            mod.setSenha(jPasswordField2.getText());
            dao.Salvar(mod);
            preecherTabela("select * from acesso order by Login");
            jPasswordField1.setText("");
            jPasswordField2.setText("");
            jPasswordField1.setEnabled(false);
            jPasswordField2.setEnabled(false);
            jComboBox2.setEnabled(false);
            jButton3.setEnabled(false);
            jButton1.setEnabled(true);
            jButton5.setEnabled(false);
        }else{
            mod.setID((String) jComboBox2.getSelectedItem());
            mod.setLogin(jTextField2.getText());
            mod.setSenha(jPasswordField1.getText());
            mod.setSenha(jPasswordField2.getText());
            dao.Alterar(mod);
            preecherTabela("select * from acesso order by login");
            jPasswordField1.setText("");
            jPasswordField2.setText("");
            jPasswordField1.setEnabled(false);
            jPasswordField2.setEnabled(false);
            jComboBox2.setEnabled(false);
            jButton3.setEnabled(false);
            jButton1.setEnabled(true);
            jButton5.setEnabled(false);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane,"As senhas não correspondem" );
        }
    }//GEN-LAST:event_jButton2ActionPerformed

